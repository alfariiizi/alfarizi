import fs from "node:fs/promises";
import path from "node:path";

import { formatResumeBuildTimestamp } from "../src/app/resume/utils.js";

const outputPath = path.resolve("src/lib/resume/build-info.js");
const timestamp = formatResumeBuildTimestamp(new Date());

const contents = `// This file is generated by script/write-resume-build-info.mjs.\nexport const RESUME_BUILD_TIMESTAMP = ${JSON.stringify(timestamp)};\n`;

await fs.mkdir(path.dirname(outputPath), { recursive: true });
await fs.writeFile(outputPath, contents);

console.log(`Wrote resume build info: ${timestamp}`);

